# Использование lambda в качестве ключа (key) для сортировки сложных данных
students = [('Иван', 22), ('Анна', 20), ('Петр', 23)]

# Сортируем студентов по возрасту (второй элемент кортежа, индекс 1)
sorted_students = sorted(students, key=lambda student: student[1])
print("Студенты, отсортированные по возрасту:", sorted_students)